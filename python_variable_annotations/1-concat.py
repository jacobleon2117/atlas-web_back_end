#!/usr/bin/env python3
"""
This module provides a function to concatenate two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings then return the result(s).

    Args:
        str1 (str): The first string to be concatenated.
        str2 (str): The second string to be concatenated.

    Returns:
        str: The concatenated string.
    """
    return str1 + str2
